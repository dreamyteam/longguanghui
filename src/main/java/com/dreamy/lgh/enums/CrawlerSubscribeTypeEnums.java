public enum CrawlerSubscribeTypeEnums {
    yes("true", "是"),
    no("false", "否");

    private String type;

    private String description;

    CrawlerSubscribeTypeEnums(String type, String description) {
        this.type = type;
        this.description = description;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
